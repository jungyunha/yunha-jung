package calcdemo;

public class CalcNav {
	public double sum(double x, double y) {
		double sum = 0;
		sum = x + y;
		return sum;
	}

	public double sub(double x, double y) {
		double sub = 0;
		sub = x - y;
		return sub;
	}

	public double multi(double x, double y) {
		double multi = 0;
		multi = x * y;
		return multi;
	}

	public double divide(double x, double y) {
		double divide = 0;
		divide = x / y;
		return divide;
	}
}
